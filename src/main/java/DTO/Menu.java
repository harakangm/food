package DTO;

public class Menu {
	private String food_name;
	private int price;
	private String mtr_inf;
	private String clr_inf;
	private String food_img;
	
	public String getFood_name() {
		return food_name;
	}
	public void setFood_name(String food_name) {
		this.food_name = food_name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getMtr_inf() {
		return mtr_inf;
	}
	public void setMtr_inf(String mtr_inf) {
		this.mtr_inf = mtr_inf;
	}
	public String getClr_inf() {
		return clr_inf;
	}
	public void setClr_inf(String clr_inf) {
		this.clr_inf = clr_inf;
	}
	public String getFood_img() {
		return food_img;
	}
	public void setFood_img(String food_img) {
		this.food_img = food_img;
	}
	
	
	
}
